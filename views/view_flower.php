    Направление «Учёба»-  Обеспечение непрерывного психотерапевтического 
процесса включающего в себя  обучающие тренинговые занятия.
Мотивационная групповая  и индивидуальная психотерапия направленная на формирование ЗОЖ, обучение компьютерной грамоте, изучение государственного языка, обучение пациентов навыков швейного и столярного  мастерства, занятия с представителями духовных конфессий. 
 Учебный процесс направлен на построение конструктивной модели поведения в социуме, формирование чувства внутренней устойчивости  и умение планировать позитивный жизненный сценарий, снятие эмоциональных барьеров, повышение адекватности собственного представления о себе и восприятия общества. 
 Занятия духовного развития с приглашение Имама по средам и  Святого отца по пятницам по запросам пациентов.
