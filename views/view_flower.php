Направление «Спорт» - В нашем центре акцент сделан на кинезитерапию.  (от  kinesis  -  движение,  therapia  -  лечение)  -  естественный метод  восстановления  работы  организма    воздействием  на  систему  мышц, связок,  суставов.  
В  кинезитерапию  входят занятия  футболом,  дающие  выход  негативной  энергии,  развивающие  умение работать  в  команде,  восстанавливающие    мышечную  систему.  Занятия в тренажерном  зале - восстанавливают мышцы человека, укрепляют силу воли, делают человека  сильнее.  Утренние пробежки - улучшают  кровообращение,  укрепляют  опорно-двигательный  аппарат,  улучшают  самочувствие  человека. Обливания холодной водой на улице стимулируют деятельность иммунной и эндокринной систем; обогащают организм энергией, зарядом бодрости, улучшают настроение,  приучают  к здоровому образу жизни.
Пословица «В здоровом теле - здоровый дух!»- не просто слова, это житейская мудрость, которую мы предлагаем воспринимать как инструкцию.   
