Направление «Распорядок»- Одним из главных этапов реабилитации это формирование умений планирования, анализа, оценки и корректировки своего распорядка дня. Выработка навыков самодисциплины, самоконтроля, ответственного выбора для эффективности изменений поведения наркозависимых лиц. Принятие ответственности за свой выбор, активная жизненная позиция, партнерская взаимодействие между администрацией центра и пациентами, что позволяет существенно повысить эффективность мотивации здорового образа жизни.    
</div>

<div id="flower3">
Направление «Труд» - Обеспечение непрерывного социально-трудового тренинга является незаменимым элементом в комплексе лечения наркозависимых. В задачи трудотерапии входит постепенное прививание элементарных умений труда, адекватное восприятие любой работы и возможности выполнять ее ежедневно. Осознание ценности своего труда и важности его результата приводят к выработке уважения работы других людей, развивает положительное отношение к деятельности. Помогает к развитию самостоятельности и эффективности межличностных коммуникаций.   
</div>

